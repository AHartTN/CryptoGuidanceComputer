/**
 * @file InputMode.ts
 * @description Enum for DSKY input modes (Verb, Noun, Program, Data, None).
 */

// InputMode enum for DSKY input state
export enum InputMode {
  /** Verb input mode */
  Verb = "verb",
  /** Noun input mode */
  Noun = "noun",
  /** Program input mode */
  Prog = "prog",
  /** Data entry mode */
  Data = "data",
  /** No input mode */
  None = "none",
}
